function procura() {

    color = document.getElementById("search-input").value;

    if (color === "Sunlight Crab") {
        window.location.href = "../../pages/produtos.html#orange"
    }
    else if (color === "Midnight Crab") {
        window.location.href = "../../pages/produtos.html#purple"
    }
    else if (color === "Black Crab") {
        window.location.href = "../../pages/produtos.html#black"
    }

} 